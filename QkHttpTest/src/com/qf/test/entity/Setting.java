package com.qf.test.entity;

public class Setting {
	private String method;
	private String sourcepath;
	private String sourcetemplet;
	private String targetpath;
	private String targettemplet;

	public String getMethod() {
		return method;
	}

	public void setMethod(String method) {
		this.method = method;
	}

	public String getSourcepath() {
		return sourcepath;
	}

	public void setSourcepath(String sourcepath) {
		this.sourcepath = sourcepath;
	}

	public String getSourcetemplet() {
		return sourcetemplet;
	}

	public void setSourcetemplet(String sourcetemplet) {
		this.sourcetemplet = sourcetemplet;
	}

	public String getTargetpath() {
		return targetpath;
	}

	public void setTargetpath(String targetpath) {
		this.targetpath = targetpath;
	}

	public String getTargettemplet() {
		return targettemplet;
	}

	public void setTargettemplet(String targettemplet) {
		this.targettemplet = targettemplet;
	}
}
